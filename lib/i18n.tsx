interface I18nCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
}

const Ctx = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("zh");

  useEffect(() => {
    const saved = (localStorage.getItem("zc_lang") as Lang | null) ?? null;
    if (saved === "zh" || saved === "en") setLangState(saved);
    else if (typeof navigator !== "undefined" && !navigator.language.startsWith("zh")) setLangState("en");
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    try {
      localStorage.setItem("zc_lang", l);
      document.documentElement.lang = l === "zh" ? "zh-CN" : "en";
    } catch {
      // ignore
    }
  };

  const t = (key: string) => DICT[lang][key] ?? DICT.zh[key] ?? key;

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export function useI18n() {
  const c = useContext(Ctx);
  if (!c) throw new Error("useI18n must be used within I18nProvider");
  return c;
}
